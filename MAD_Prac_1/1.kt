fun main(){
    var integer:Int = 8
    println("Integer Value : $integer")

    var float:Float = 2.3f
    println("Float Value : $float")

    var double:Double = 34.5
    println("Double Value : $double")

    var string:String = "BV"
    println("String Value : $string")

    var charcter:Char = 'B'
    println("Character Value : $charcter")

    var boolean:Boolean = true
    println("Boolean Value : $boolean")

    var longvalue:Long = 22012011050
    println("Long Value : $longvalue")

    var shortvalue:Short = -85
    println("Short Value : $shortvalue")

    var byte:Byte = 23
    println("Byte Value : $byte")
}